#!/bin/bash

# ------------------------------------------------------------------------------
# SOURCE.SH
# ------------------------------------------------------------------------------
set -e
SOURCE=$INSTALLER/000-source

# ------------------------------------------------------------------------------
# INIT
# ------------------------------------------------------------------------------
echo
echo "-------------------------- SOURCE -------------------------"

# ------------------------------------------------------------------------------
# PRE-SOURCE PACKAGES
# ------------------------------------------------------------------------------
export DEBIAN_FRONTEND=noninteractive
apt-get $APT_PROXY -y install procps

# ------------------------------------------------------------------------------
# SET GLOBAL VARIABLES
# ------------------------------------------------------------------------------
# Architecture
ARCH=$(dpkg --print-architecture)
echo "export ARCH=$ARCH" >> $SOURCE

# always return true
true
